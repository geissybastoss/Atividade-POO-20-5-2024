package exercicio9;

public class Carro {
    
      // Atributos privados
      private String marca;
      private int ano;
  
      // Método set para marca
      public void setMarca(String marca) {
          this.marca = marca;
      }
  
      // Método get para marca
      public String getMarca() {
          return this.marca;
      }
  
      // Método set para ano
      public void setAno(int ano) {
          this.ano = ano;
      }
  
      // Método get para ano
      public int getAno() {
          return this.ano;
      }
}
