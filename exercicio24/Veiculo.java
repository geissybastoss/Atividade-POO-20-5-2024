package exercicio24;

public class Veiculo {
    
        // Atributo privado que armazena a marca do veículo
        private String marca;

        // Construtor da classe Veiculo que inicializa a marca do veículo
        public Veiculo(String marca) {
            this.marca = marca;
        }
    
        // Método getter para obter a marca do veículo
        public String getMarca() {
            return marca;
        }
}
